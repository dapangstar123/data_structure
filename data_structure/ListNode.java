package data_structure;

/**
 * 
 * @author pang
 *
 */
public class ListNode {
	int val;
	ListNode next;

	public ListNode(int val) {
		super();
		this.val = val;
	}

	public int getVal() {
		return val;
	}

	public void setVal(int val) {
		this.val = val;
	}

	public ListNode getNext() {
		return next;
	}

	public void setNext(ListNode next) {
		this.next = next;
	}

}
